#include <stdio.h>

int main() {
    // w06-02-01
    int i = 1, j = 2, k;
    printf("w06-02-01:\n");                                       //แสดงค่า "w06-02-01:"
    printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → i = %d, j = %d\n", i, j);   //1.แสดงค่าบรรทัดที่1 กำหนดค่าเริ่มต้นของ i,j
    k = i + j;                                                    //คำนวณค่า k = i + j
    printf("บรรทัดที่ 2: k = i + j = %d + %d = %d\n", i, j, k);     //2.แสดงผลบรรทัดที่2 แสดงค่าk จาก k= i+j
    i = i + (k * j);                                             //คำนวณค่า i = i +(k * j)
    printf("บรรทัดที่ 3: i = i + (k * j) = %d + (%d * %d) = %d\n", i + (k * j), k, j, i);   //3.แสดงผลบรรทัดที่3 ค่าi จาก i+(k * j)  
    j = i / 2;                                                   //คำนวณค่า j = i / 2
    printf("บรรทัดที่ 4: j = i / 2 = %d / 2 = %d\n", i, j);        //4.แสดงผลบรรทัดที่ 4 แสดงค่าj จาก i/2
    k = j % 2;                                                  //คำนวณค่า k = j % 2
    printf("บรรทัดที่ 5: k = j %% 2 = %d %% 2 = %d\n", j, k);     //5.แสดงผลบรรทัดที่ 5 แสดงค่า K จาก j%%2 
    i = (j + k) * 3;                                           //คำนวณค่า i = (j + k) * 3
    printf("บรรทัดที่ 6: i = (j + k) * 3 = (%d + %d) * 3 = %d\n", j, k, i);     //6.แสดงผลบรรทัดที่ 6 แสดงค่า i จาก j+k
    printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → i = %d, j = %d, k = %d\n\n", i, j, k);   //7.แสดง ค่า i,j,k
                                                                            // w06-02-02
    double x = 1.0, y = 2.0, z;                                             //กำหนดค่า x และ y
    printf("w06-02-02:\n");                                                 //แสดงค่า "w06-02-02:"
    printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → x = %.1f, y = %.1f\n", x, y);    //2.1แสดงค่าบรรทัดที่ 1 ค่า x = 1.0 y = 2.0
    x = y + 5.0;                                                      //คำนวณค่า x = y+5.0
    printf("บรรทัดที่ 2: x = y + 5.0 = %.1f + 5.0 = %.1f\n", y, x);     //2.2แสดงผลบรรทัดที่ 2 แสดงค่า x จาก y+5.0
    y = x / 2.0;                                                     //คำนวณค่า y = x / 2.0
    printf("บรรทัดที่ 3: y = x / 2.0 = %.1f / 2.0 = %.1f\n", x, y);    //2.3แสดงค่า y จาก x/2.0
    y = (x * 3.0) + 4.0;                                            //คำนวณค่า y = (x * 3.0) + 4.0
    printf("บรรทัดที่ 4: y = (x * 3.0) + 4.0 = (%.1f * 3.0) + 4.0 = %.1f\n", x, y);    //2.4แสดงค่า y จาก (x*3.0) +4.0      
    x = -0.5 * y;                                                      //คำนวณค่า x = -0.5 * y
    printf("บรรทัดที่ 5: x = -0.5 * y = -0.5 * %.1f = %.1f\n", y, x);    //2.5แสดงผลบรรทัดที่ 5 แสดงค่า x จาก -0.5*y
    z = x + y;                                                        //คำนวณค่า z = x + y
    printf("บรรทัดที่ 6: z = x + y = %.1f + %.1f = %.1f\n", x, y, z);   //2.6แสดงค่าบรรทัดที่ 6 แสดงค่า z จากx+y
    printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → x = %.1f, y = %.1f, z = %.1f\n", x, y, z);   //2.7แสดงผลบรรทัดที่ 7 แสดงค่า x,y,z
    return 0;                          //คืนค่า 0; เสร็จสิ้นกระบวนการ
}
