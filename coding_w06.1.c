                                    //3.คำนวณค่าและแสดงผล(5 - 17 = -12); 
                                    //4.คำนวณค่าและแสดงผล(8.5 * 4.0 = 34.00(ตอบในรูปทศนิยม 2 ตำแหน่ง));
                                    //5.คำนวณค่าและแสดงผล("5 * 8.5 = 42.50 (ตอบในรูปทศนิยม 2 ตำแหน่ง));  
                                    //6.คำนวณค่าและแสดงผล(8.5 / 4.0 = 2.12(ตอบในรูปทศนิยม 2 ตำแหน่ง));  
                                    //7.คำนวณค่าและแสดงผล(17 / 8.5 = 2.00(ตอบในรูปทศนิยม 2 ตำแหน่ง));
                                    //8.คำนวณค่าและแสดงผล(5 % 17 = 5);
